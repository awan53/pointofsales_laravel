<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;

class AuthController extends Controller
{
    //
    public function showLogin(){
        return view('auth.login');
    }

    public function login(Request $request){
        $credentials = $request->validate([
            'email' => 'required|email',
            'password' => 'required',
        ]);


        $user = \App\Models\User::where('email', $request->email)->first();
    
    if (!$user) {
        return back()->withErrors(['email' => 'Email tidak terdaftar di sistem.']);
    }

    if (!\Illuminate\Support\Facades\Hash::check($request->password, $user->password)) {
        return back()->withErrors(['email' => 'Password salah (Hash tidak cocok).']);
    }

    if (Auth::attempt($credentials)){
        $request->session()->regenerate();
        return redirect()->intended('home');
    }
    
    return back()->withErrors(['email' => 'Gagal melakukan login.']);
        // if (Auth::attempt($credentials)){
        //     $request->session()->regenerate();
        //     return redirect()->intended('home');
        // }
        // return back()->withErrors(['email' => 'Email atau password anda salah']);

    }

    public function logout(Request $request){
        Auth::logout();
        $request->session()->invalidate();
        $request->session()->regenerateToken();
        return redirect('/login');
    }
}
